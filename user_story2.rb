# {{PROBLEM}} Method Design Recipe

## 1. Describe the Problem


  # As a user
  # So that I can improve my grammar
  # Verify text starts with caps and ends with punctuation


## 2. Design the Method Signature

# method name = grammar_check
# parameter = sentence to check over
# return = whether caps, punctuation both or neither are present

## 3. Create Examples as Tests

_Make a list of examples of what the method will take and return._

```ruby
grammar_check("This sentence") => Needs punctuation
grammar_check("this sentence.") => Needs caps
grammar_check("this sentence) => Needs caps and punctuation
grammar_check("This sentence.) => Perfect
```

_Encode each example as a test. You can add to the above list as you go._

## 4. Implement the Behaviour

_After each test you write, follow the test-driving process of red, green, refactor to implement the behaviour._


